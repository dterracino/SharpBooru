namespace Cyotek.Windows.Forms
{
  /// <summary>
  ///   Specifies the size of the background texture grid.
  /// </summary>
  public enum ImageBoxGridScale
  {
    /// <summary>
    ///   Half of size.
    /// </summary>
    Tiny,

    /// <summary>
    ///   Default size.
    /// </summary>
    Small,

    /// <summary>
    ///   50% increase of default size.
    /// </summary>
    Medium,

    /// <summary>
    ///   100% increase of default size.
    /// </summary>
    Large
  }
}
